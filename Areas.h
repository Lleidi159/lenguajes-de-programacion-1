float circulo(int valorB)
{
	return 3.141516 * valorB * valorB;
}

float pentagono(int valorA, int valorB)
{
    return valorA * valorB / 2;
}

float trapacio(float valorA, float valorB,float valorC)
{
    return ((valorA + valorB)/ 2) * valorC;
}

float rectangulo(float valorA, float valorB)
{
    return valorA * valorB;
}
